package July8th_WrapperClasses;

public class Lab_AutoboxingandUnboxing {
    public static void main(String[] args) {
        int a=10;
        Integer b=a;
        System.out.println("int a to Integer b  "+b);

        int c=b;
        System.out.println("int c=b;  is "+c);

        char d='g';
        Character e=d;

        System.out.println(" Character e=d;   "+e);

        char f=e;
        System.out.println("char f=e; "+f);
    }
}
